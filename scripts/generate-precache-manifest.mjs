import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const excludedFiles = new Set(["/_headers", "/precache-manifest.js", "/sw.js"]);

export async function listFiles(directory, relativeDirectory = "") {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const relativePath = path.posix.join(relativeDirectory.replaceAll(path.sep, "/"), entry.name);
    const absolutePath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      files.push(...(await listFiles(absolutePath, relativePath)));
    } else if (entry.isFile()) {
      files.push(`/${relativePath}`);
    }
  }

  return files;
}

export function routeFromIndexFile(file) {
  if (file === "/index.html") return "/";
  if (!file.endsWith("/index.html") || file.startsWith("/_next/")) return null;
  return file.slice(0, -"index.html".length);
}

export function getDynamicAssetPaths(loadableManifest) {
  const dynamicAssets = new Set();

  for (const entry of Object.values(loadableManifest)) {
    if (!entry || typeof entry !== "object" || !Array.isArray(entry.files)) continue;

    for (const file of entry.files) {
      if (typeof file === "string" && file.length > 0) {
        dynamicAssets.add(`/_next/${file.replaceAll("\\", "/").replace(/^\/+/, "")}`);
      }
    }
  }

  return dynamicAssets;
}

export async function generatePrecacheManifest({ rootDirectory = projectRoot } = {}) {
  const outputDirectory = path.join(rootDirectory, "out");
  const buildIdPath = path.join(rootDirectory, ".next", "BUILD_ID");
  const loadableManifestPath = path.join(rootDirectory, ".next", "react-loadable-manifest.json");
  const manifestPath = path.join(outputDirectory, "precache-manifest.js");
  const buildId = (await readFile(buildIdPath, "utf8")).trim();
  if (!buildId) {
    throw new Error("Next.js BUILD_ID is empty; cannot create a versioned precache manifest.");
  }

  const loadableManifest = JSON.parse(await readFile(loadableManifestPath, "utf8"));
  const dynamicAssets = getDynamicAssetPaths(loadableManifest);
  const assets = (await listFiles(outputDirectory))
    .filter((file) => !excludedFiles.has(file) && !dynamicAssets.has(file))
    .sort();
  const routes = assets.map(routeFromIndexFile).filter(Boolean).sort();
  const manifest = { buildId, assets, routes };
  const source = `// Generated by scripts/generate-precache-manifest.mjs.\nself.__FINANCIAL_CALC_PRECACHE_MANIFEST__ = ${JSON.stringify(
    manifest,
    null,
    2
  )};\n`;

  await writeFile(manifestPath, source, "utf8");
  return manifest;
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  const manifest = await generatePrecacheManifest();
  console.log(
    `Generated precache manifest for ${manifest.buildId}: ${manifest.assets.length} assets, ${manifest.routes.length} routes.`
  );
}
